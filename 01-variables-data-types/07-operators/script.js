// 1. Aritmetic Operators
let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 5 % 5;

// Concatenation
x = 'Hello ' + 'World';

// Exponent
x = 8 ** 3;

// Increment
x += 1; // x = x + 1
x++;

// Decrement
x -= 1;
x--; // x = x - 1

// 2. Assignment Operators
x = 10;

x += 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;

// 3. Comparison Operators
x = 2 == 2;
x = 2 === '2';

x = 2 != 2;

x = 2 > 10;
x = 2 < 10;
x = 2 >= 10;
x = 2 <= 10;

console.log(x);